      <div class="col-xs-10 col-xs-offset-1 col-md-6 col-md-offset-1">
        <p class="sec-text">
          The Light Keeper's Cottage restoration was completed in 2001. Over 250 volunteers put in more than 300,000 hours of labor to finish the restoration and ready the boat for tours. Since 2001 volunteers have restored the staircase of the lighthouse, built a boathouse on the island, constructed a replica of the original boat (which sits in the boathouse) and reconstructed the privy.
        </p>
        <p class="sec-text">
          Today the tour is operated with over 125 volunteers, whose jobs include island housekeepers, gardeners, tour boat captains, crew and tour guides, and gift shop sales and service personnel. Volunteers also install the portable walkways and docks every spring and remove them every fall. To date, there have been over 20,538 visitors to the island.
        </p>
          <p class="sec-text">
          The Chantry Island Lightstation Tower was constructed in the years 1855 through to 1859 under the authority of the Board of Works, Canada West, and is recognized as one of the Imperial Towers. The Imperial Towers were six of the earliest lighthouses built by the Canadian government on Lake Huron. The Chantry Island Lightstation Tower is also known as the Lighttower, Tower, Phare de l'île Chantry, Tour de phare, however, most notably as Chantry Island Lighthouse. The wording lightstation is used to refer to the actual lighthouse, the property on which the lighted tower is situated, the tower's light and refers to any equipment or other buildings located on the island property. The actual island is located approximately a mile off the shores of the town of Southampton, Ontario, and south of the mouth of the Saugeen River on Lake Huron. Surrounded by water, the island is 63-hectare (160-acre) in size.
        </p>
          <p class="sec-text">
          Chantry Island Lighthouse was first lit on April 1, 1859. Underwater shoals of massive granite boulders made navigation in the area dangerous; there are many accounts from the 1800’s on of disasters and lost lives. The lighthouse was valuable in its day, but now, buoys and modern navigational tools safely guide boats through the area.
        </p>

